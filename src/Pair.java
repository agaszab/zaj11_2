public class Pair {

    private Object first;
    private Object secont;

    // Object[] pair = new Object[2];


    public Pair(Object first, Object secont) {
        this.first = first;
        this.secont = secont;
    }

    public Object getFirst() {
        return first;
    }

    public void setFirst(Object first) {
        this.first = first;
    }

    public Object getSecont() {
        return secont;
    }

    public void setSecont(Object secont) {
        this.secont = secont;
    }
}



